const images = [
  { name: 'clear', label: '맑음 - 시야 양호', bg: '#87CEEB', text: '#1a365d' },
  { name: 'moderate', label: '보통 - 약간 흐림', bg: '#B0C4DE', text: '#2d3748' },
  { name: 'hazy', label: '나쁨 - 미세먼지 감지', bg: '#9CA3AF', text: '#374151' },
  { name: 'very-hazy', label: '매우나쁨 - 심한 안개', bg: '#6B7280', text: '#d1d5db' },
];

for (const img of images) {
  const svg = createMockSVG(img.label, img.bg, img.text);
  writeFileSync(`public/mock-cctv/${img.name}.svg`, svg);
  console.log(`Created: public/mock-cctv/${img.name}.svg`);
}

// JPEG placeholder도 생성 (fallback)
for (const img of images) {
  const jpeg = createMinimalJPEG(128, 128, 128);
  writeFileSync(`public/mock-cctv/${img.name}.jpg`, jpeg);
  console.log(`Created: public/mock-cctv/${img.name}.jpg`);
}

console.log('Done! Mock CCTV images generated.');
